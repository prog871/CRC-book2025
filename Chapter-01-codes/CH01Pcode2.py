# A Python code
import math
# input variable
a= 0.95
# calculations 
c1= math.sin(a)
c2= math.cos(a)
c3= math.log(a)
c4= math.sqrt(a) #square root
c5= math.asin(a) #inverse sine (arcsine) 
# printing the results
print('c1 = ', c1)
print('c2 = ', c2)
print('c3 = ', c3)
print('c4 = ', c4)
print('c5 = ', c5)
